public class Camp
{
    private Integer camp_id;
    private String camp_name;
    private String camp_description;
    private String camp_pic;

    public Integer getCamp_id()
    {
        return camp_id;
    }

    public void setCamp_id(Integer camp_id)
    {
        this.camp_id = camp_id;
    }

    public String getCamp_name()
    {
        return camp_name;
    }

    public void setCamp_name(String camp_name)
    {
        this.camp_name = camp_name;
    }

    public String getCamp_description()
    {
        return camp_description;
    }

    public void setCamp_description(String camp_description)
    {
        this.camp_description = camp_description;
    }

    public String getCamp_pic()
    {
        return camp_pic;
    }

    public void setCamp_pic(String camp_pic)
    {
        this.camp_pic = camp_pic;
    }
}
