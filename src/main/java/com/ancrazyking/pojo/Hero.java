public class Hero
{
    private Integer hero_id;
    private String hero_honour;
    private String hero_name;
    private String hero_sex;
    private Integer camp_id;
    private String hero_pic;
    private String hero_description;

    public Integer getHero_id()
    {
        return hero_id;
    }

    public void setHero_id(Integer hero_id)
    {
        this.hero_id = hero_id;
    }

    public String getHero_honour()
    {
        return hero_honour;
    }

    public void setHero_honour(String hero_honour)
    {
        this.hero_honour = hero_honour;
    }

    public String getHero_name()
    {
        return hero_name;
    }

    public void setHero_name(String hero_name)
    {
        this.hero_name = hero_name;
    }

    public String getHero_sex()
    {
        return hero_sex;
    }

    public void setHero_sex(String hero_sex)
    {
        this.hero_sex = hero_sex;
    }

    public Integer getCamp_id()
    {
        return camp_id;
    }

    public void setCamp_id(Integer camp_id)
    {
        this.camp_id = camp_id;
    }

    public String getHero_pic()
    {
        return hero_pic;
    }

    public void setHero_pic(String hero_pic)
    {
        this.hero_pic = hero_pic;
    }

    public String getHero_description()
    {
        return hero_description;
    }

    public void setHero_description(String hero_description)
    {
        this.hero_description = hero_description;
    }
}
